<template>
  <st-panel app class="shop-member-list">
    <div slot="title">
      <st-input-search
        placeholder="输入用户姓名、手机号"
        v-model="$searchQuery.keyword"
        @search="onKeywordsSearch('keyword', $event)"
      />
    </div>
    <div slot="prepend">
      <st-search-panel @search="onSearchNative" @reset="onSearhReset">
        <st-search-panel-item label="用户级别：">
          <st-search-radio
            v-model="$searchQuery.member_level"
            :options="memberLevel"
          />
        </st-search-panel-item>
        <st-search-panel-item label="来源方式：">
          <st-search-radio
            v-model="$searchQuery.register_way"
            :options="sourceList"
          />
        </st-search-panel-item>
        <st-search-panel-item label="注册时间：">
          <st-range-picker
            :disabledDays="180"
            :value="selectTime"
          ></st-range-picker>
        </st-search-panel-item>
        <div slot="more">
          <st-search-panel-item label="入会时间：">
            <st-range-picker
              :disabledDays="180"
              :value="selectMemberTime"
            ></st-range-picker>
          </st-search-panel-item>
          <st-search-panel-item label="员工跟进：">
            <st-search-radio
              v-model="$searchQuery.is_follow"
              :options="isFollow"
            />
          </st-search-panel-item>
        </div>
      </st-search-panel>
    </div>
    <div class="mg-t24 mg-b16">
      <st-button
        type="primary"
        @click="addUser()"
        class="shop-member-list-button"
        v-if="auth.add"
        icon="add"
      >
        添加用户
      </st-button>
      <!-- NOTE: 导入 -->
      <!-- <st-button class="shop-member-list-button" v-if="auth.import">导入用户</st-button> -->
      <st-button
        v-if="auth.tag"
        class="shop-member-list-button"
        :disabled="!selectedRows.length"
        v-modal-link="{
          name: 'shop-add-lable',
          props: {
            memberIds: selectedRowKeys
          }
        }"
      >
        加标签
      </st-button>
      <a-popover placement="bottom">
        <template slot="content">
          <div v-if="auth.bindCoach" class="shop-member-list-button-div">
            <label
              class="shop-member-list-button-label"
              v-modal-link="{
                name: 'shop-distribution-coach',
                props: {
                  memberIds: selectedRowKeys
                }
              }"
            >
              分配教练
            </label>
          </div>
          <div v-if="auth.bindSalesman" class="shop-member-list-button-div">
            <label
              class="shop-member-list-button-label"
              v-modal-link="{
                name: 'shop-distribution-sale',
                props: {
                  memberIds: selectedRowKeys
                }
              }"
            >
              分配销售
            </label>
          </div>
        </template>
        <st-button
          v-if="auth.bindCoach && auth.bindSalesman"
          class="shop-member-list-button"
          :disabled="selectedRows.length > 0 ? false : true"
        >
          分配员工
        </st-button>
      </a-popover>
      <!-- NOTE: 导出 -->
      <!-- <st-button v-if="auth.export" :disabled='isSelectedDisabled' class="shop-member-list-button">批量导出</st-button> -->
    </div>
    <st-table
      :columns="columns"
      :loading="loading.getListInfo"
      :scroll="{ x: 1400 }"
      :alertSelection="{ onReset: onSelectionReset }"
      :rowSelection="{ selectedRowKeys, onChange: onSelectionChange }"
      rowKey="member_id"
      :page="page"
      @change="onTableChange"
      :dataSource="list"
    >
      <div slot="image_face" slot-scope="text, record">
        <span class="st-preview-item st-preview-item--cover" v-viewer>
          <img
            v-if="record.image_face"
            class="st-image-face"
            :src="record.image_face.url"
            :data-src="record.image_face.url"
          />
        </span>
      </div>
      <div slot="member_name" slot-scope="text, record">
        <a
          href="javascript:;"
          v-if="record.auth['shop:member:member|get']"
          @click="infoFunc(record)"
        >
          {{ text }}
        </a>
        <span v-else>{{ text }}</span>
      </div>
      <div slot="action" slot-scope="text, record">
        <st-table-actions>
          <a
            v-if="record.auth['shop:member:member|get']"
            @click="infoFunc(record)"
          >
            详情
          </a>
          <a
            v-if="record.auth['shop:member:member|edit']"
            @click="edit(record)"
          >
            编辑
          </a>
          <a
            v-if="record.auth['shop:member:member|bind_coach']"
            @click="onDistributionCoach(record)"
          >
            分配教练
          </a>
          <a
            v-if="record.auth['shop:member:member|bind_salesman']"
            @click="onDistributionSale(record)"
          >
            分配销售
          </a>
          <a
            v-if="record.auth['shop:member:member|bind_card']"
            v-modal-link="{
              name: 'shop-binding-entity-card',
              props: { record },
              on: {
                success: refeshPage
              }
            }"
          >
            绑实体卡
          </a>
          <a
            v-if="record.auth['shop:member:member|rebind_card']"
            v-modal-link="{ name: 'shop-missing-card', props: { record } }"
          >
            重绑实体卡
          </a>
          <!-- <a v-if="record.auth['shop:member:member|transfer']" v-modal-link="{ name: 'shop-transfer-shop', props:{record}, on: {
                 success: refeshPage
               } }">转店</a> -->
          <a
            v-if="record.auth['shop:member:member|frozen']"
            v-modal-link="{
              name: 'shop-frozen',
              props: { record },
              on: {
                success: refeshPage
              }
            }"
          >
            冻结用户
          </a>
          <a
            v-if="record.auth['shop:member:member|unbind_wechat']"
            @click="onRemoveBind(record)"
          >
            解除微信绑定
          </a>
        </st-table-actions>
      </div>
    </st-table>
  </st-panel>
</template>
<script>
import moment from 'moment'
import { cloneDeep, filter } from 'lodash-es'
import { UserService } from '@/services/user.service'
import { ListService } from './list.service'
import tableMixin from '@/mixins/table.mixin'
import { columns } from './list.config'
import ShopAddLable from '@/views/biz-modals/shop/add-lable'
import ShopBindingEntityCard from '@/views/biz-modals/shop/binding-entity-card'
import ShopDistributionCoach from '@/views/biz-modals/shop/distribution-coach'
import ShopDistributionSale from '@/views/biz-modals/shop/distribution-sale'
import ShopFrozen from '@/views/biz-modals/shop/frozen'
import ShopMissingCard from '@/views/biz-modals/shop/missing-card'
export default {
  name: 'memberList',
  mixins: [tableMixin],
  modals: {
    ShopAddLable,
    ShopBindingEntityCard,
    ShopDistributionCoach,
    ShopDistributionSale,
    ShopFrozen,
    ShopMissingCard
  },
  serviceInject() {
    return {
      listService: ListService,
      userService: UserService
    }
  },
  rxState() {
    const user = this.userService
    return {
      loading: this.listService.loading$,
      shopMemberEnums: user.shopMemberEnums$,
      reserveEnums: user.reserveEnums$,
      memberEnums: user.memberEnums$,
      auth: this.listService.auth$,
      list: this.listService.list$,
      page: this.listService.page$
    }
  },
  data() {
    return {
      dateFormat: 'YYYY-MM-DD',
      expand: false,
      sourceRegisterList: [],
      consumption: [],
      selectDataList: [],
      selectedRowKeys: [],
      selectedRows: [],
      selectTime: {
        startTime: {
          showTime: false,
          disabledBegin: null,
          placeholder: '开始日期',
          disabled: false,
          value: null,
          format: 'YYYY-MM-DD',
          change: $event => {}
        },
        endTime: {
          showTime: false,
          placeholder: '结束日期',
          disabled: false,
          value: null,
          format: 'YYYY-MM-DD',
          change: $event => {}
        }
      },
      selectMemberTime: {
        startTime: {
          showTime: false,
          disabledBegin: null,
          placeholder: '开始日期',
          disabled: false,
          value: null,
          format: 'YYYY-MM-DD',
          change: $event => {}
        },
        endTime: {
          showTime: false,
          placeholder: '结束日期',
          disabled: false,
          value: null,
          format: 'YYYY-MM-DD',
          change: $event => {}
        }
      }
    }
  },
  computed: {
    columns,
    memberLevel() {
      let list = [{ value: -1, label: '全部' }]
      if (!this.shopMemberEnums.member_level) return list
      Object.entries(this.shopMemberEnums.member_level.value).forEach(o => {
        list.push({ value: +o[0], label: o[1] })
      })
      return list
    },
    isFollow() {
      let list = [{ value: -1, label: '全部' }]
      if (!this.memberEnums.is_follow) return list
      Object.entries(this.memberEnums.is_follow.value).forEach(o => {
        list.push({ value: +o[0], label: o[1] })
      })
      return list
    },
    defaultRegValue() {
      if (!this.$searchQuery.register_start_time) return []
      return [
        moment(this.$searchQuery.register_start_time, this.dateFormat),
        moment(this.$searchQuery.register_stop_time, this.dateFormat)
      ]
    },
    defaultBeMemberValue() {
      if (!this.$searchQuery.be_member_start_time) return null
      return [
        moment(this.$searchQuery.be_member_start_time, this.dateFormat),
        moment(this.$searchQuery.be_member_stop_time, this.dateFormat)
      ]
    },
    sourceList() {
      let list = [{ value: -1, label: '全部' }]
      if (!this.memberEnums.source_channel) return list
      Object.entries(this.memberEnums.source_channel.value).forEach(o => {
        list.push({ value: +o[0], label: o[1] })
      })
      return list
    }
  },
  mounted() {
    this.sourceRegisters()
    this.setSearchData()
  },
  watch: {
    query(newVal) {
      this.setSearchData()
    }
  },

  methods: {
    refeshPage() {
      this.$router.reload()
    },
    sourceRegisters() {
      this.listService.getMemberSourceRegisters().subscribe(status => {
        this.sourceRegisterList = status
      })
    },
    // 查询
    onSearchNative() {
      this.$searchQuery.register_start_time = this.selectTime.startTime.value
        ? `${this.selectTime.startTime.value.format('YYYY-MM-DD')}`
        : ''
      this.$searchQuery.register_stop_time = this.selectTime.endTime.value
        ? `${this.selectTime.endTime.value.format('YYYY-MM-DD')}`
        : ''
      this.$searchQuery.be_member_start_time = this.selectMemberTime.startTime
        .value
        ? `${this.selectMemberTime.startTime.value.format('YYYY-MM-DD')}`
        : ''
      this.$searchQuery.be_member_stop_time = this.selectMemberTime.endTime
        .value
        ? `${this.selectMemberTime.endTime.value.format('YYYY-MM-DD')}`
        : ''
      this.onSearch()
    },
    // 设置searchData
    setSearchData() {
      this.selectTime.startTime.value = this.$searchQuery.register_start_time
        ? cloneDeep(moment(this.$searchQuery.register_start_time))
        : null
      this.selectTime.endTime.value = this.$searchQuery.register_stop_time
        ? cloneDeep(moment(this.$searchQuery.register_stop_time))
        : null
      this.selectMemberTime.startTime.value = this.$searchQuery
        .be_member_start_time
        ? cloneDeep(moment(this.$searchQuery.be_member_start_time))
        : null
      this.selectMemberTime.endTime.value = this.$searchQuery
        .be_member_stop_time
        ? cloneDeep(moment(this.$searchQuery.be_member_stop_time))
        : null
    },
    // 分配教练
    onDistributionCoach(record) {
      if (record.follow_coach) {
        this.$confirm({
          title: '提示信息',
          content: `该用户已存在跟进${this.$c('coach')}，是否确认替换？`,
          onOk: () => {
            this.$modalRouter.push({
              name: 'shop-distribution-coach',
              props: {
                memberIds: [record.member_id],
                coachId: record.follow_coach_id
              },
              on: {
                success: () => {
                  this.$router.reload()
                }
              }
            })
          },
          onCancel() {}
        })
      } else {
        this.$modalRouter.push({
          name: 'shop-distribution-coach',
          props: {
            memberIds: [record.member_id],
            coachId: record.follow_coach_id
          },
          on: {
            success: () => {
              this.$router.reload()
            }
          }
        })
      }
    },
    // 分配销售
    onDistributionSale(record) {
      if (record.follow_salesman) {
        this.$confirm({
          title: '提示信息',
          content: '该用户已存在跟进销售，是否确认替换？',
          onOk: () => {
            this.$modalRouter.push({
              name: 'shop-distribution-sale',
              props: {
                memberIds: [record.member_id],
                saleId: record.follow_salesman_id
              },
              on: {
                success: () => {
                  this.$router.reload()
                }
              }
            })
          },
          onCancel() {}
        })
      } else {
        this.$modalRouter.push({
          name: 'shop-distribution-sale',
          props: {
            memberIds: [record.member_id],
            saleId: record.follow_salesman_id
          },
          on: {
            success: () => {
              this.$router.reload()
            }
          }
        })
      }
    },
    onRemoveBind(record) {
      this.$confirm({
        title: '提示信息',
        content: '确认解绑选中的会员关系？',
        onOk: () => {
          this.listService.removeWechatBind(record.member_id).subscribe(() => {
            this.$router.reload()
          })
        },
        onCancel() {}
      })
    },
    edit(record) {
      this.$router.push({
        name: 'shop-member-edit',
        query: { id: record.member_id }
      })
    },
    addUser() {
      this.$router.push({ name: 'shop-member-add' })
    },
    infoFunc(record) {
      this.$router.push({
        name: 'shop-member-info-basic',
        query: { id: record.member_id }
      })
    },
    moment,
    handleReset() {
      let self = this
      for (let prop in self.form) {
        self.form[prop] = ''
      }
      this.$refs.stSeleter.handleResetItem()
      this.consumption = []
      this.$router.push({ query: {} })
    },
    toggle() {
      this.expand = !this.expand
    },
    onSelectionReset() {
      this.selectedRowKeys = []
      this.selectedRows = []
    },
    onSelectionChange(keys, selectedRows) {
      this.selectedRowKeys = keys
      this.selectedRows = selectedRows
      this.selectDataList = selectedRows.map(item => {
        return item.id
      })
    },
    queryFunc() {
      this.$router.push({ query: this.form })
    }
  }
}
</script>
