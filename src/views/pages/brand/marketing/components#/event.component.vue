<template>
  <div :class="event()">
    <st-form labelWidth="71px">
      <st-form-item label="推广活动数" :class="event('activity-wrapper')">
        <a-radio-group v-model="number">
          <a-radio :value="1">1</a-radio>
          <a-radio :value="2">2</a-radio>
          <a-radio :value="3">3</a-radio>
          <a-radio :value="4">4</a-radio>
        </a-radio-group>
      </st-form-item>
      <st-form-item label="活动展示" :class="event('activity-show-title')">
        <div
          v-for="(li, index) in list[number]"
          :key="index"
          :class="['col-' + li.span, { 'clear-float': index === 2 }]"
        >
          <div :class="event('box')">
            <st-image-upload
              @change="imageUploadChange($event, index)"
              width="100%"
              height="85px"
              :list="li.image_url ? [li] : li.filelist"
              :sizeLimit="2"
              placeholder="添加活动图片"
              :numLimit="1"
            >
              <a-icon
                type="plus-circle"
                theme="filled"
                :style="{
                  fontSize: '24px',
                  verticalAlign: 'super',
                  marginRight: '8px',
                  color: '#9BACB9'
                }"
              />
              <div :class="event('uploadtip-wrap')">
                <div class="st-image-upload__placeholder">添加活动图片</div>
                <span :class="event('uploadtip')">
                  {{
                    li.span === 24
                      ? '大小不超过5M，建议尺寸670*120'
                      : '建议尺寸327*120'
                  }}
                </span>
              </div>
            </st-image-upload>
            <div class="st-form-item-wrapper">
              <st-form-item labelWidth="46px" label="标题">
                <a-input
                  placeholder="选填"
                  v-model="li.module_name"
                  maxlength="20"
                ></a-input>
              </st-form-item>
              <st-form-item labelWidth="46px" label="链接">
                <a-select
                  placeholder="请输入链接的活动"
                  @select="actSelect(li, $event)"
                  v-model="li.activity_id"
                >
                  <a-select-option
                    v-for="(act, i) in actList"
                    :disabled="act.isover"
                    :key="i"
                    :value="act.id"
                  >
                    {{ act.activity_name }}
                  </a-select-option>
                </a-select>
              </st-form-item>
            </div>
          </div>
        </div>
      </st-form-item>
    </st-form>
  </div>
</template>
<script>
import { H5WrapperService } from '@/views/pages/brand/setting/mina/components#/h5/h5-wrapper.service'
import { cloneDeep } from 'lodash-es'
import { ActivityService } from '../activity.service'
export default {
  bem: {
    event: 'activity-event-component'
  },
  serviceInject() {
    return {
      h5WrapperService: H5WrapperService,
      activityService: ActivityService
    }
  },
  rxState() {
    return {
      eventInfo: this.h5WrapperService.eventInfo$,
      activityList: this.activityService.actList$
    }
  },
  data() {
    return {
      info: {},
      list: {
        1: [],
        2: [],
        3: [],
        4: []
      },
      actList: [],
      selected: [],
      filelist: [],
      link: '',
      number: 0,
      span: 24
    }
  },
  created() {
    this.initList()
  },
  mounted() {
    if (this.eventInfo.length) {
      let number = this.eventInfo.length
      this.number = number
      this.list[number] = cloneDeep(this.eventInfo)
      this.actList = cloneDeep(this.activityList)
      this.list[number].forEach(item => {
        if (!this.actList.some(act => act.id === item.activity_id)) {
          this.actList.push({
            activity_name: item.activity_name,
            activity_type: item.activity_type,
            id: item.activity_id,
            isover: true
          })
        }
      })
    } else {
      this.number = 0
    }
  },
  watch: {
    list: {
      deep: true,
      handler(newVal) {
        let number = this.number
        this.h5WrapperService.SET_H5INFO(newVal[number], 3)
      }
    },
    number(newValue) {
      this.h5WrapperService.SET_H5INFO(this.list[newValue], 3)
    }
  },
  methods: {
    initList() {
      let list = {
        1: [],
        2: [],
        3: [],
        4: []
      }
      list[1].push({
        image_url: '',
        title: '',
        link: '',
        span: 24
      })
      for (let i = 0; i < 2; i++) {
        list[2].push({
          image_url: '',
          title: '',
          link: '',
          span: 12
        })
      }
      for (let i = 0; i < 3; i++) {
        if (i === 2) {
          list[3].push({
            image_url: '',
            title: '',
            link: '',
            span: 24
          })
        } else {
          list[3].push({
            image_url: '',
            title: '',
            link: '',
            span: 12
          })
        }
      }
      for (let i = 0; i < 4; i++) {
        list[4].push({
          image_url: '',
          title: '',
          link: '',
          span: 12
        })
      }
      this.list = list
    },
    actSelect(item, value) {
      let selected = this.actList.filter(it => it.id === value)[0]
      item.activity_type = selected.activity_type
      item.activity_name = selected.activity_name
    },
    imageUploadChange(e, index) {
      if (e.length) {
        let number = this.number
        this.list[number][index].image_url = e[0].image_url
        this.list[number][index].image_key = e[0].image_key
      }
    },
    numberChange(e) {
      let num = e.target.value
      let list = []
      if (num === 1) {
        list.push({
          image_url: '',
          title: '',
          link: '',
          span: 24
        })
      } else if (num === 2 || num === 4) {
        for (let i = 0; i < num; i++) {
          list.push({
            image_url: '',
            title: '',
            link: '',
            span: 12
          })
        }
      } else if (num === 3) {
        for (let i = 0; i < num; i++) {
          if (i === 2) {
            list.push({
              image_url: '',
              title: '',
              link: '',
              span: 24
            })
          } else {
            list.push({
              image_url: '',
              title: '',
              link: '',
              span: 12
            })
          }
        }
      }
      this.list = list
    }
  }
}
</script>
